    private void laiTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_laiTblMouseClicked
        // TODO add your handling code here:
        int r = laiTbl.getSelectedRow();
        String click = laiTbl.getModel().getValueAt(r, 1).toString();
        String sqlQuery = "SELECT * FROM hasil_audit WHERE no_LAI='" + click + "'";

        try {
            pst = conn.prepareStatement(sqlQuery);
            rs = pst.executeQuery();

            if (rs.next()) {
                String idKlienValue = rs.getString(1);
                IDKlienTxt.setText(idKlienValue);
                String noLAIValue = rs.getString(2);
                noLAITxt.setText(noLAIValue);
                Date tglLAIValue = rs.getDate(3);
                tglLAI.setDate(tglLAIValue);
                int tahunTakwimValue = rs.getInt(4);
                tahunTxt.setYear(tahunTakwimValue);
                String goPublikIndex = rs.getString(5);
                goPublikCb.setSelectedItem(goPublikIndex);
                String opiniValue = rs.getString(6);
                opiniTxt.setText(opiniValue);
                Date periodeAwalValue = rs.getDate(7);
                periodeAwalTxt.setDate(periodeAwalValue);
                Date periodeAkhirValue = rs.getDate(8);
                periodeAkhirTxt.setDate(periodeAkhirValue);
                String restatementIndexValue = rs.getString(9);
                restatementCb.setSelectedItem(restatementIndexValue);
                String noRestatValue = rs.getString(10);
                noRestatTxt.setText(noRestatValue);
                String IdApSebelumValue = rs.getString(11);
                namaApSebelumnya.setText(IdApSebelumValue);
                String tahunAPValue = rs.getString(12);
                tahunAPTxt.setText(tahunAPValue);
                String keteranganValue = rs.getString(13);
                keteranganTxt.setText(keteranganValue);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        isEditing = true;
        IDKlienTxt.setEnabled(isEditing);
        noLAITxt.setEnabled(isEditing);
        tglLAI.setEnabled(isEditing);
        tahunTxt.setEnabled(isEditing);
        goPublikCb.setEnabled(isEditing);
        opiniTxt.setEnabled(isEditing);
        periodeAwalTxt.setEnabled(isEditing);
        periodeAkhirTxt.setEnabled(isEditing);
        keteranganTxt.setEnabled(isEditing);
        restatementCb.setEnabled(isEditing);
        noRestatTxt.setEnabled(isEditing);
        namaApSebelumnya.setEnabled(isEditing);
        tahunAPTxt.setEnabled(isEditing);
        updateBtn.setEnabled(true);
    }//GEN-LAST:event_laiTblMouseClicked

