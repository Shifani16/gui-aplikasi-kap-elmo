    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String idKlienValue = IDKlienTxt.getText();
        String noLAIValue = noLAITxt.getText();
        Date tglLAIValue = tglLAI.getDate();
        int tahunTakwim = tahunTxt.getYear();
        String goPublikIndex = (String) goPublikCb.getSelectedItem();
        String opiniValue = opiniTxt.getText();
        Date periodeAwalValue = periodeAwalTxt.getDate();
        Date periodeAkhirValue = periodeAkhirTxt.getDate();
        String keteranganValue = keteranganTxt.getText();
        String restatementIndex = (String) restatementCb.getSelectedItem();
        String noRestatValue = noRestatTxt.getText();
        String namaApSebelumValue = namaApSebelumnya.getText();
        String tahunAPValue = tahunAPTxt.getText();
        

        String sqlQuery = "UPDATE hasil_audit SET ID_klien=?, tgl_LAI=?, tahun_takwim=?, go_publik=?, opini=?, periode_awal=?, periode_akhir=?, restatement=?, no_restatement=?, nama_ap_sebelumnya=?, tahun_ap_sebelumnya=?, keterangan=? WHERE no_LAI=?";

        try {
            pst = conn.prepareStatement(sqlQuery);
            pst.setString(1, idKlienValue);
            pst.setDate(2, new java.sql.Date(tglLAIValue.getTime()));
            pst.setInt(3, tahunTakwim);
            pst.setString(4, goPublikIndex);
            pst.setString(5, opiniValue);
            pst.setDate(6, new java.sql.Date(periodeAwalValue.getTime()));
            pst.setDate(7, new java.sql.Date(periodeAkhirValue.getTime()));
            pst.setString(8, restatementIndex);
            pst.setString(9, noRestatValue);
            pst.setString(10, namaApSebelumValue);
            pst.setString(11, (tahunAPValue.isEmpty() ? "" : tahunAPValue));
            pst.setString(12, keteranganValue);
            pst.setString(13, noLAIValue);

            pst.execute();
            populateTable();
            JOptionPane.showMessageDialog(null, "Data Successfully Updated");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        isEditing = false;
        updateFields();
        updateButtons();


    }//GEN-LAST:event_updateBtnActionPerformed

